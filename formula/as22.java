//. A pond is 50 m long, 30 m wide and 2 m deep. Find the capacity of the pond in cubic metre.

class Test {
	public static void main(String[] args) {
		int length, breath, height, a;
		length = 50;
		breath = 30;
		height = 2;

		a = length * breath * height;

		System.out.println(+a + " m capacity of pond  ");
	}
}